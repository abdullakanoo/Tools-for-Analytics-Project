(SELECT * from yellow_taxi_trips
UNION ALL
SELECT * from uber_taxi_trips)
WHERE strftime('%Y',date_and_time) = "2009"
GROUP BY DATE(date_and_time)
ORDER BY count(strftime('%Y',date_and_time)) DESC
LIMIT 10


